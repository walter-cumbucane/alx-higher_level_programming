class TestBase_initialization(unittest.TestCase):
    """A class for testing the Base class instances initialization"""

    def test_init_with_none(self):
        """Tests when the object is initialized without arguments"""
        b1 = Base()
        b2 = Base()
        self.assertEqual(b1.id, b2.id - 1)

    def test_init_with_arguments(self):
        """Tests when the object is initialized with arguments"""
        b1 = Base(4)
        b2 = Base(-5000)
        b3 = Base(10000000)
        b4 = Base(None)
        b5 = Base(None)
        self.assertEqual(b1.id, 4)
        self.assertEqual(b2.id, -5000)
        self.assertEqual(b3.id, 10000000)
        self.assertEqual(b4.id, b5.id - 1)

    def test_nb_instances_private(self):
        """Tests for nb instances used as private attribute"""
        with self.assertRaises(AttributeError):
            print(Base().__nb_instances)

    def test_id_str(self):
        """Tests for string id's"""
        self.assertEqual("Test", Base("Test").id)

    def test_id_float(self):
        """Tests for float id's"""
        self.assertEqual(1.3, Base(1.3).id)

    def test_id_complex(self):
        """Tests for complex id's"""
        self.assertEqual(complex(3), Base(complex(3)).id)

    def test_id_bool(self):
        """Tests for boolean id's"""
        self.assertEqual(False, Base(False).id)

    def test_id_list(self):
        """Tests for lists id's"""
        self.assertEqual([-1, -2, 7], Base([-1, -2, 7]).id)

    def test_tuple_id(self):
        self.assertEqual((1, 2), Base((1, 2)).id)

    def test_id_set(self):
        self.assertEqual({4, 20, 31}, Base({4, 20, 31}).id)

    def test_id_frozenset(self):
        self.assertEqual(frozenset({1, 2, 3}), Base(frozenset({1, 2, 3})).id)

    def test_id_range(self):
        self.assertEqual(range(200), Base(range(200)).id)

    def test_id_bytes(self):
        self.assertEqual(b'ALX', Base(b'ALX').id)

    def test_id_bytearray(self):
        self.assertEqual(bytearray(b'abcefg'), Base(bytearray(b'abcefg')).id)

    def test_id_memoryview(self):
        self.assertEqual(memoryview(b'abcefg'), Base(memoryview(b'abcefg')).id)

    def test_id_inf(self):
        self.assertEqual(float('inf'), Base(float('inf')).id)

    def test_id_Nan(self):
        self.assertNotEqual(float('nan'), Base(float('nan')).id)

    def test_two_arguments(self):
        self.assertRaises(TypeError, Base, 3, 5)

